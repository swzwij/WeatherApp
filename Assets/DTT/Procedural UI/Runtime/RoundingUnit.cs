namespace DTT.UI.ProceduralUI
{
	/// <summary>
	/// The different units used as representation in the inspector.
	/// </summary>
	public enum RoundingUnit
	{
		/// <summary>
		/// Everything in a range between 0 and 1.
		/// </summary>
		PERCENTAGE = 0,

		/// <summary>
		/// Uses Unity's world units.
		/// </summary>
		WORLD = 1,
	}
} 